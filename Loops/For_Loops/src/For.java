public class For {
	public static void main(String[] args) {
		
		//1. Initial condition 2. Conditions for execution 3. Condition executed once after every iteration
		for(int myAge = 0; myAge <= 20; myAge++) {
			System.out.printf("Cyril is %d this year.\n", myAge);
		}
		//you can use print f
		//first argument (%d) is a format specifier and the second (myAge) is a value
		// \n for new line

	}
}
