public class usingStrings {
	public static void main(String[] args) {
		int jerseyNumber = 4;

		String object = "jersey";
		String owner = "My";
		String id = "number";
		String blank = " ";
		
		String info = owner + blank + object + blank + id;
		
		System.out.println(info + blank + "is: " + jerseyNumber + ".");
		System.out.println("My jersey number is: 4.");
		
		
	}

}
